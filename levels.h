/*******************************************************************************

level data

Levels can be from a minimum of 5 x 5 tiles up to 16 x 16 tiles.
Levels that are wider than 10 tiles and / or higher than 5 tiles
will have scrolling.

a level starts with a size byte:

(level width - 1) << 4) | (level height -1)

followed with the positions for the player, the banana and the exit object.
The format of the tile positions is as following:

(tile X << 4) | (Tile Y) where a tile positon of 0x00 is the top left corner.

After these special objects. the data followes for the 7 tiles types in the
this order:

ladder, solid tile, death tile, left block, right block, up block and down block.

the format for each of these tiles are:

A length byte containing the number of a tiles used followed by the tile positions.
if there are no tiles of the type used then the length byte is zero.

check the remarks at level 1 for more info.

*******************************************************************************/


